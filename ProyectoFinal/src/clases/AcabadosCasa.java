package clases;

public class AcabadosCasa {
	
	String tipo_piso, tipo_griferia, tipo_iluminacion;
	boolean techo_aislante;
	boolean bano_insonoro;
	public String getTipo_piso() {
		return tipo_piso;
	}
	public void setTipo_piso(String tipo_piso) {
		this.tipo_piso = tipo_piso;
	}
	public String getTipo_griferia() {
		return tipo_griferia;
	}
	public void setTipo_griferia(String tipo_griferia) {
		this.tipo_griferia = tipo_griferia;
	}
	public String getTipo_iluminacion() {
		return tipo_iluminacion;
	}
	public void setTipo_iluminacion(String tipo_iluminacion) {
		this.tipo_iluminacion = tipo_iluminacion;
	}
	public boolean isTecho_aislante() {
		return techo_aislante;
	}
	public void setTecho_aislante(boolean techo_aislante) {
		this.techo_aislante = techo_aislante;
	}
	public boolean isBano_insonoro() {
		return bano_insonoro;
	}
	public void setBano_insonoro(boolean bano_insonoro) {
		this.bano_insonoro = bano_insonoro;
	}
	
	

}
